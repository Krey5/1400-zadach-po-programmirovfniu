x = float(input("Введите x: "))
y = float(input("Введите y: "))

# Для области а)
if 0 <= x <= 2 and 0 <= y <= 2:
    print("Точка попадает в область I (а)")
else:
    print("Точка не попадает в область I (а)")

# Для области б)
if -2 <= x <= 0 and -2 <= y <= 0:
    print("Точка попадает в область I (б)")
else:
    print("Точка не попадает в область I (б)")
