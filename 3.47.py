h = int(input("Часы (0 < h ≤ 23): "))
m = int(input("Минуты (0 ≤ m ≤ 59): "))
s = int(input("Секунды (0 ≤ s ≤ 59): "))
# Часовая стрелка за 12 часов проходит 360°, за 1 час — 30°, за 1 минуту — 0.5°, за 1 секунду — 0.5/60
total_seconds = h * 3600 + m * 60 + s
angle = total_seconds * (360 / (12 * 3600))  # градусы
print("Угол часовой стрелки от начала суток:", angle, "градусов")
