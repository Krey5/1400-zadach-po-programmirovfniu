# Из числа x вычли последнюю цифру, разделили на 10, приписали цифру слева → 237
# Пусть x = abc, тогда операция: (ab) / 10 → a.b? Нет, вернее: (abc - c) / 10 = ab, потом приписали c слева → cab = 237
# Значит c = 2, a = 3, b = 7 → x = 372
# Проверим: 372 - 2 = 370, /10 = 37, приписали 2 слева → 237. Верно.
print("Число x = 372")
