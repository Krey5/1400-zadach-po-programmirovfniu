r1 = float(input("Сопротивление первого участка: "))
u1 = float(input("Напряжение на первом участке: "))
r2 = float(input("Сопротивление второго участка: "))
u2 = float(input("Напряжение на втором участке: "))

i1 = u1 / r1
i2 = u2 / r2

if i1 < i2:
    print("Ток меньше на первом участке")
else:
    print("Ток меньше на втором участке")
