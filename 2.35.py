V1 = float(input("Скорость первого автомобиля (км/ч): "))
V2 = float(input("Скорость второго автомобиля (км/ч): "))
S = float(input("Начальное опережение (км): "))

# Проверка, что V1 > V2
if V1 <= V2:
    print("Ошибка: первый автомобиль должен быть быстрее второго!")
else:
    time = 0.5  
    
    # Расстояние, на которое увеличится разрыв за 30 минут
    Sr = (V1 - V2) * time
    
    
    St = S + Sr
    
    print(St)
