num = int(input("Натуральное число: "))
last_digit = num % 10

if last_digit % 2 == 0:
    print("Число оканчивается четной цифрой")
else:
    print("Число не оканчивается четной цифрой")

if last_digit % 2 == 1:
    print("Число оканчивается нечетной цифрой")
else:
    print("Число не оканчивается нечетной цифрой")
